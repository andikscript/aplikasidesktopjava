    public void kosongkanJumlah(){
        enableJumlah.setSelected(false);
        tambah.setEnabled(false);
        jumlah.setEditable(false);
    }
    
    public void kosongkanTotal(){
        totalKeseluruhan = 0;
        total.setText("");
    }
    
    public void kosongkanArray(){
        c.arrayIdPenjualan.clear();
        c.arrayIdMotif.clear();
        c.arrayIdUkuran.clear();
        c.arrayJumlah.clear();
        c.arrayHarga.clear();
        c.arrayTotal.clear();
    }
    
    public void kosongkanPelanggan(){
        nama.setText("");
        alamat.setText("");
        kontak.setText("");
        nama.setEditable(false);
        kontak.setEditable(false);
        alamat.setEditable(false);
        enable.setSelected(false);
    }
    
    public void kosongkanTabel(){
        int baris = model.getRowCount();
        for (int a = 0; a < baris; a++) {
            model.removeRow(0);
        }
    }   
    
