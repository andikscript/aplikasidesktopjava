    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        if (enable.isSelected() && nama.getText() != "") {
            c.namaPelanggan = nama.getText();
            c.alamatPelanggan = alamat.getText();
            c.kontakPelanggan = kontak.getText();
            SimpanVariable.namaPenerima = nama.getText();
            SimpanVariable.alamatPenerima = alamat.getText();
            SimpanVariable.kontakPenerima = kontak.getText();

            c.setHitungPelanggan();
            c.setIdPelanggan();
            c.insertPelanggan();

            kosongkanTabel();
            kosongkanPelanggan();
            kosongkanTotal();

            c.runInsert();
            kosongkanArray();
            no = 0;
        } else {
            JOptionPane.showMessageDialog(null, "Masukkan Data Pelanggan Terlebih dahulu");
        }
        
    }//GEN-LAST:event_submitActionPerformed

